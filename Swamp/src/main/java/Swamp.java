
public class Swamp {
	
	private int locationTX;
	private int locationTY;
	
	public Swamp(int locationTX, int locationTY) {
		
		this.locationTX = (locationTX);
		this.locationTY = (locationTY);
	}
	
	public int getLocationTX() {
		return locationTX;
	}

	public void setLocationTX(int locationTX) {
		this.locationTX = locationTX;
	}

	public int getLocationTY() {
		return locationTY;
	}

	public void setLocationTY(int locationTY) {
		this.locationTY = locationTY;
	}

}